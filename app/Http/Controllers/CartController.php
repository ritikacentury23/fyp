    public function order(Request $request)
{
    $request->validate([
        'first_name' => 'required|string',
        'last_name' => 'required|string',
        'email' => 'required|email',
        'phone' => 'required|string',
        'address1' => 'required|string',
        'city' => 'required|string',
        'country' => 'required|string',
        'post_code' => 'required|string',
    ]);

    $carts = Cart::where('user_id', auth()->id())->where('order_id', null)->get();

    if ($carts->isEmpty()) {
        return redirect()->route('cart')->with('error', 'Your cart is empty.');
    }

    $subtotal = 0;
    foreach ($carts as $cart) {
        $subtotal += $cart->price * $cart->quantity;
    }

    $couponDiscount = 0;
    if (session()->has('coupon')) {
        $couponDiscount = session('coupon')['value'];
    }

    $total = $subtotal - $couponDiscount;

    // Create order
    $order = Order::create([
        'user_id' => auth()->id(),
        'order_number' => 'ORD-' . strtoupper(uniqid()),
        'sub_total' => $subtotal,
        'coupon' => $couponDiscount,
        'total_amount' => $total,
        'quantity' => $carts->sum('quantity'),
        'payment_method' => 'cash',
        'payment_status' => 'unpaid',
        'status' => 'process',
        'first_name' => $request->first_name,
        'last_name' => $request->last_name,
        'email' => $request->email,
        'phone' => $request->phone,
        'address1' => $request->address1,
        'address2' => $request->address2,
        'city' => $request->city,
        'country' => $request->country,
        'post_code' => $request->post_code,
        'landmark' => $request->landmark,
        'order_notes' => $request->order_notes,
    ]);

    // Assign order_id to cart items
    foreach ($carts as $cart) {
        $cart->update(['order_id' => $order->id]);
    }

    // Send order confirmation email
    Mail::to($request->email)->send(new OrderSuccessMail($order));

    // Clear coupon session
    session()->forget('coupon');

    return redirect()->route('order.success', $order->order_number);
}

public function success($order_number)
{
    $order = Order::where('order_number', $order_number)
        ->where('user_id', auth()->id())
        ->firstOrFail();

    $cartItems = Cart::where('order_id', $order->id)->with('product')->get();

    return view('order-success', compact('order', 'cartItems'));
}
